public class DataTypes {
    public static void main(String[] args) {
        byte var1 = 100;
        float var2 = 3.14F;
        double var3 = 2.71828;
        char var5 = 'A';
        boolean var6 = true; 
        String var7 = "Hello, Java!";
        System.out.println("Integer: " + var1);
        System.out.println("Float: " + var2);
        System.out.println("Double: " + var3);
        System.out.println("Character: " + var5);
        System.out.println("Boolean: " + var6);
        System.out.println("String: " + var7);
     }
}